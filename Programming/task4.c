#include <stdio.h>

// Добавьте заголовочные файлы, если нужно
typedef struct {
    unsigned char r, g, b;
} color_t;

color_t* get_lightest_grey(color_t *arr, size_t len) {
  // Функция, возвращающая самый светлый серый цвет, точнее, указатель на него
  // Инициализируем переменную самого светлого серого цвета
  color_t *lightest_grey = NULL;

  // Проходим по всем элементам массива
  for (size_t i = 0; i < len; i++) {
    // Если текущий элемент является самым светлым серым цветом, обновляем переменную
    if (arr[i].r == arr[i].g && arr[i].g == arr[i].b && (lightest_grey == NULL || arr[i].r > lightest_grey->r)) {
      lightest_grey = &arr[i];
    }
  }

  // Возвращаем указатель на самый светлый серый цвет
  return lightest_grey;
}

int main() {
  // Создаем массив RGB-цветов
  color_t arr[] = {
    {255, 255, 254}, //это ж не серый, получается
    {255, 255, 0},
    {120, 0, 120},
    {0, 255, 255},
    {0, 100, 0},
    {50, 0, 0},
    {0, 0, 255},
    {169+1, 169+1, 169+1}, //вот это выведется на экран
    {127, 127, 127},
  };

  // Получаем самый светлый серый цвет
  color_t *lightest_grey = get_lightest_grey(arr, sizeof(arr) / sizeof(arr[0]));

  // Если самый светлый серый цвет найден, выводим его
  if (lightest_grey != NULL) {
    printf("lightest grey: (%d, %d, %d)\n", lightest_grey->r, lightest_grey->g, lightest_grey->b);
  } else {
    printf("grey color not found.\n");
  }

  return 0;
}

